package weblogic.diagnostics.snmp.mib;

public class WLSMibMetadataException extends Exception {
   public WLSMibMetadataException(String arg0) {
      super(arg0);
   }

   public WLSMibMetadataException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public WLSMibMetadataException(Throwable arg0) {
      super(arg0);
   }
}
