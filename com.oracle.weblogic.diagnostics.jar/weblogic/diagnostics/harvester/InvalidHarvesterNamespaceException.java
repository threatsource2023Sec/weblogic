package weblogic.diagnostics.harvester;

public class InvalidHarvesterNamespaceException extends Exception {
   public InvalidHarvesterNamespaceException() {
   }

   public InvalidHarvesterNamespaceException(String arg0) {
      super(arg0);
   }

   public InvalidHarvesterNamespaceException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public InvalidHarvesterNamespaceException(Throwable arg0) {
      super(arg0);
   }
}
