package weblogic.diagnostics.harvester;

public class HarvesterRuntimeException extends RuntimeException {
   public HarvesterRuntimeException() {
   }

   public HarvesterRuntimeException(String arg0) {
      super(arg0);
   }

   public HarvesterRuntimeException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public HarvesterRuntimeException(Throwable arg0) {
      super(arg0);
   }
}
