package weblogic.diagnostics.snmp.agent;

public class SNMPAgentToolkitException extends Exception {
   public SNMPAgentToolkitException(String arg0) {
      super(arg0);
   }

   public SNMPAgentToolkitException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public SNMPAgentToolkitException(Throwable arg0) {
      super(arg0);
   }
}
