package weblogic.security.service;

public class ResourceCreationException extends Exception {
   public ResourceCreationException() {
   }

   public ResourceCreationException(String arg0) {
      super(arg0);
   }

   public ResourceCreationException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ResourceCreationException(Throwable arg0) {
      super(arg0);
   }
}
