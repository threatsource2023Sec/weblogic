package weblogic.security.service;

public class DeployHandleCreationException extends Exception {
   public DeployHandleCreationException() {
   }

   public DeployHandleCreationException(String arg0) {
      super(arg0);
   }

   public DeployHandleCreationException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public DeployHandleCreationException(Throwable arg0) {
      super(arg0);
   }
}
