package weblogic.security.service;

public class ResourceRemovalException extends Exception {
   public ResourceRemovalException() {
   }

   public ResourceRemovalException(String arg0) {
      super(arg0);
   }

   public ResourceRemovalException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ResourceRemovalException(Throwable arg0) {
      super(arg0);
   }
}
