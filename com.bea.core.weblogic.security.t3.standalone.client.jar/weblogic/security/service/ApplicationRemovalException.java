package weblogic.security.service;

public class ApplicationRemovalException extends Exception {
   public ApplicationRemovalException() {
   }

   public ApplicationRemovalException(String arg0) {
      super(arg0);
   }

   public ApplicationRemovalException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ApplicationRemovalException(Throwable arg0) {
      super(arg0);
   }
}
