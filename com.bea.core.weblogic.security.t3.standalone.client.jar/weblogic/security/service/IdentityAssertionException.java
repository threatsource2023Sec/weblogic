package weblogic.security.service;

public class IdentityAssertionException extends Exception {
   public IdentityAssertionException() {
   }

   public IdentityAssertionException(String arg0) {
      super(arg0);
   }

   public IdentityAssertionException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public IdentityAssertionException(Throwable arg0) {
      super(arg0);
   }
}
