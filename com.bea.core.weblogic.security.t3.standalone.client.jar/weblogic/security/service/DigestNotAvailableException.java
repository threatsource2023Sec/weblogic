package weblogic.security.service;

public class DigestNotAvailableException extends Exception {
   public DigestNotAvailableException() {
   }

   public DigestNotAvailableException(String arg0) {
      super(arg0);
   }

   public DigestNotAvailableException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public DigestNotAvailableException(Throwable arg0) {
      super(arg0);
   }
}
