package weblogic.security.service;

public class ApplicationVersionRemovalException extends Exception {
   public ApplicationVersionRemovalException() {
   }

   public ApplicationVersionRemovalException(String arg0) {
      super(arg0);
   }

   public ApplicationVersionRemovalException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ApplicationVersionRemovalException(Throwable arg0) {
      super(arg0);
   }
}
