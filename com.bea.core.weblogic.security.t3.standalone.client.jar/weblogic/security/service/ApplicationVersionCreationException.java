package weblogic.security.service;

public class ApplicationVersionCreationException extends Exception {
   public ApplicationVersionCreationException() {
   }

   public ApplicationVersionCreationException(String arg0) {
      super(arg0);
   }

   public ApplicationVersionCreationException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ApplicationVersionCreationException(Throwable arg0) {
      super(arg0);
   }
}
