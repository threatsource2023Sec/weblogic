package weblogic.security.service;

public class ConsumptionException extends Exception {
   public ConsumptionException() {
   }

   public ConsumptionException(String arg0) {
      super(arg0);
   }

   public ConsumptionException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public ConsumptionException(Throwable arg0) {
      super(arg0);
   }
}
